def milla_to_kilometro(longitud):
    
    """
    Función que toma como parámetro una longitud (millas) y la convierte 
    en kilometros
    
    Función: longitud * 1.6093445

    Returns:
        El resultado de convertir (longitud) en kilometros
    """
    
    resultado = 0
    resultado = longitud * 1.6093445
    return resultado

