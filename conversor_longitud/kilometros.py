def kilometro_to_metro(longitud):
    
    """
    Función que toma como parámetro una longitud (kilometros) y la convierte 
    en metros
    
    Función: longitud * 1000

    Returns:
        El resultado de convertir (longitud) en metros
    """
    
    resultado = 0
    resultado = longitud * 1000
    return resultado
    

