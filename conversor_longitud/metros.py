def metro_to_kilometro(longitud):
    
    """
    Función que toma como parámetro una longitud (metros) y la convierte 
    en kilometros
    
    Función: longitud / 1000

    Returns:
        El resultado de convertir (longitud) en kilometros
    """
    
    resultado = 0
    resultado = longitud / 1000
    return resultado

